package com.hisi.model;

import javax.persistence.*;

@Table(name = "hisi_parameter")
public class HisiParameter {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer id;

    @Column(name = "video_save_time")
    private String videoSaveTime;

    @Column(name = "photo_save_time")
    private String photoSaveTime;

    @Column(name = "Screen_interval_time")
    private String screenIntervalTime;

    /**
     * @return id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return video_save_time
     */
    public String getVideoSaveTime() {
        return videoSaveTime;
    }

    /**
     * @param videoSaveTime
     */
    public void setVideoSaveTime(String videoSaveTime) {
        this.videoSaveTime = videoSaveTime;
    }

    /**
     * @return photo_save_time
     */
    public String getPhotoSaveTime() {
        return photoSaveTime;
    }

    /**
     * @param photoSaveTime
     */
    public void setPhotoSaveTime(String photoSaveTime) {
        this.photoSaveTime = photoSaveTime;
    }

    /**
     * @return Screen_interval_time
     */
    public String getScreenIntervalTime() {
        return screenIntervalTime;
    }

    /**
     * @param screenIntervalTime
     */
    public void setScreenIntervalTime(String screenIntervalTime) {
        this.screenIntervalTime = screenIntervalTime;
    }
}